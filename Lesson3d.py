# Control Structures
# Selection/Conditional
# if - Single Alternative
# if else - double alternative
# Elif - multiple Alternative
num1 = -60
if num1 > 0:
    print("Its Positive") # Works for True

elif num1==0:
    print('Its Zero')   # Works for True

elif num1 < 0:
    print('Its Negative')  # Works for True

else:
    print('Error!')